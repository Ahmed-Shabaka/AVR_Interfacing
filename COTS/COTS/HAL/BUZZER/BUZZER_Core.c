void Buzzer_init(void)
{
	SET_BIT(DIO_PORTC->DDR,BUZZER);
}

void Buzzer_start(uint8 clk_times)
{
	uint8 i=0;
	
	for(i=0;i<clk_times;i++)
	{
		SET_BIT(DIO_PORTC->PORT,BUZZER);
		_delay_ms(700);
		CLEAR_BIT(DIO_PORTC->PORT,BUZZER);
		_delay_ms(500);
	}
}

